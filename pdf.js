// Browser PDF generation: opens a print page with one applicant per page.
import { allReadonly } from "./core.js";
import { sirFileBaseFromState } from "./importExport.js";

const PDF_DISCLAIMER = "Disclaimer: This is not an official mapping or government form. This PDF is only generated by a helper tool for data entry.";
const OFFLINE_WARNING = "Copy these details in original form.\nSubmit your original form to BLO.";

function esc(value) {
  return String(value ?? "").replace(/[&<>"']/g, ch => ({
    "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#039;"
  }[ch]));
}

function cleanText(value) {
  return String(value ?? "").replace(/[\r\n\t]+/g, " ").replace(/\s{2,}/g, " ").trim();
}

function formatDateForDisplay(value) {
  const raw = String(value || "").trim();
  const m = raw.match(/^(\d{4})-(\d{2})-(\d{2})$/);
  if (m) return `${m[3]}-${m[2]}-${m[1]}`;
  return raw;
}

function safePhotoSrc(value) {
  const src = String(value || "");
  return /^data:image\/(png|jpe?g|webp);base64,/i.test(src) ? src : "";
}

function field(label, value) {
  return `
    <div class="read-field">
      <span class="read-label" aria-hidden="true">${esc(label)}</span>
      <span class="read-value">${esc(cleanText(value))}</span>
    </div>`;
}

function acPartSerialTable(ac, part, sl) {
  return `
    <table class="ac-table">
      <thead><tr><th>AC no</th><th>Part no</th><th>Sl no</th></tr></thead>
      <tbody><tr><td>${esc(cleanText(ac))}</td><td>${esc(cleanText(part))}</td><td>${esc(cleanText(sl))}</td></tr></tbody>
    </table>`;
}

function dobDigits(dateValue) {
  const raw = String(dateValue || "");
  const m = raw.match(/^(\d{4})-(\d{2})-(\d{2})$/);
  const value = m ? `${m[3]}${m[2]}${m[1]}` : raw.replace(/\D/g, "").slice(0, 8);
  return value.padEnd(8, " ").slice(0, 8).split("");
}

function dobCells(dateValue) {
  return `<div class="dob-cells">${dobDigits(dateValue).map(d => `<span>${esc(d)}</span>`).join("")}</div>`;
}

function onlinePage(data) {
  const photo = safePhotoSrc(data["Photo Data"]);
  return `
    <section class="page">
      <div class="online-head">
        <h1>${esc(data.applicant_name || "Applicant")}</h1>
        ${photo ? `<img class="online-photo" src="${esc(photo)}" alt="Applicant photo">` : ""}
      </div>
      <div class="pdf-disclaimer">${esc(PDF_DISCLAIMER)}</div>
      <section class="read-section top-grid">
        ${field("EPIC ID", data["EPIC ID"])}
        ${field("Phone Number", data["Phone Number"])}
      </section>
      <section class="read-section">
        <h2>Mapping Details</h2>
        <div class="read-line two">
          ${field("Type", data["Mapping Type"])}
          ${field("Relationship", data["Mapping Relation"])}
        </div>
        <div class="read-line four">
          ${field("Mapper Name", data["Mapper Name as per 2002"] || data["Mapping Name"])}
          ${field("2002 EPIC Number", data["Mapper 2002 EPIC Number"])}
          ${field("Relative", data["Mapper Relative Name"])}
          ${field("Relation with Relative", data["Mapper Relationship with Relative"])}
        </div>
        <div class="read-line two">
          ${field("State", data["Mapping State"])}
          ${field("District", data["Mapping District"])}
        </div>
        <div class="read-line three">
          ${field("AC No", data["Mapping AC No Display"])}
          ${field("Part No", data["Mapping Part No"])}
          ${field("Sl No", data["Mapping Serial No"])}
        </div>
      </section>
      <section class="read-section">
        <h2>Applicant Details</h2>
        <div class="read-line two">
          ${field("Date of Birth", formatDateForDisplay(data["Date of Birth"]))}
          ${field("Aadhaar Number", data["Aadhaar Number"])}
        </div>
        <h3>Father</h3>
        <div class="read-line two">${field("Name", data["Father’s Name"])}${field("EPIC Number", data["Father’s EPIC Number"])}</div>
        <h3>Mother</h3>
        <div class="read-line two">${field("Name", data["Mother’s Name"])}${field("EPIC Number", data["Mother’s EPIC Number"])}</div>
        <h3>Spouse</h3>
        <div class="read-line two">${field("Name", data["Spouse’s Name"])}${field("EPIC Number", data["Spouse’s EPIC Number"])}</div>
      </section>
    </section>`;
}

function offlineMappingColumn(title, data, shouldFill) {
  const value = label => shouldFill ? cleanText(data[label]) : "";
  const relation = shouldFill ? cleanText(data["Mapping Relation"]) : "";
  const mapperName = value("Mapper Name as per 2002");
  const showRelation = relation && relation !== "Self";
  const mapperNameWithRelation = mapperName && showRelation ? `${mapperName} (${relation})` : mapperName;
  return `
    <section class="offline-col">
      <h2>${esc(title)}</h2>
      ${field("Name as per 2002", mapperNameWithRelation)}
      ${field("Relative name", value("Mapper Relative Name"))}
      ${field("Relationship with relative", value("Mapper Relationship with Relative"))}
      ${field("District", value("Mapping District"))}
      ${field("State", value("Mapping State"))}
      ${field("AC name", value("Mapping AC Name"))}
      ${field("2002 EPIC number", value("Mapper 2002 EPIC Number"))}
      ${acPartSerialTable(value("Mapping AC No"), value("Mapping Part No"), value("Mapping Serial No"))}
    </section>`;
}

function offlineDetailRow(label, valueHtml) {
  return `
    <div class="offline-detail-row">
      <div class="offline-detail-label">${esc(label)}</div>
      <div class="offline-detail-value">${valueHtml}</div>
    </div>`;
}

function offlineDetailText(label, value) {
  return offlineDetailRow(label, `<span class="read-value">${esc(cleanText(value))}</span>`);
}

function offlineApplicantDetails(data) {
  return `
    <section class="read-section offline-details">
      <h2>Applicant Details</h2>
      <div class="offline-detail-grid">
        ${offlineDetailRow("Date of birth", dobCells(data["Date of Birth"]))}
        ${offlineDetailText("Aadhaar number", data["Aadhaar Number"])}
        ${offlineDetailText("Phone number", data["Phone Number"])}
        ${offlineDetailText("Father's name", data["Father’s Name"])}
        ${offlineDetailText("Father's EPIC number", data["Father’s EPIC Number"])}
        ${offlineDetailText("Mother's name", data["Mother’s Name"])}
        ${offlineDetailText("Mother's EPIC number", data["Mother’s EPIC Number"])}
        ${offlineDetailText("Spouse's name", data["Spouse’s Name"])}
        ${offlineDetailText("Spouse's EPIC number", data["Spouse’s EPIC Number"])}
      </div>
    </section>`;
}

function offlinePage(data) {
  const isSelf = data["Mapping Relation"] === "Self";
  const header = `${cleanText(data.applicant_name || "Applicant")} (${cleanText(data["EPIC ID"])})`;
  return `
    <section class="page offline-page">
      <h1>${esc(header)}</h1>
      <div class="pdf-disclaimer">${esc(PDF_DISCLAIMER)}</div>
      <div class="offline-warning">${esc(OFFLINE_WARNING).replace(/\n/g, "<br>")}</div>
      <div class="offline-two">
        ${offlineMappingColumn("Self Mapping", data, isSelf)}
        ${offlineMappingColumn("Parent Mapping", data, !isSelf)}
      </div>
      ${offlineApplicantDetails(data)}
    </section>`;
}

