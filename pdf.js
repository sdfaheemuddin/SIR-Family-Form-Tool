function pdfApplicants(state) {
  return (state.applicants || []).filter(a => a.export_to_pdf !== false);
}

export function generatePdf(state) {
  const rows = allReadonly(pdfApplicants(state), state.people);
  if (!rows.length) throw new Error("No applicants selected for PDF export.");
  openPdfWindow({ state, title: "Online Form PDF", pagesHtml: rows.map(onlinePage).join(""), mode: "online" });
}

export function generateOfflinePdf(state) {
  const rows = allReadonly(pdfApplicants(state), state.people);
  if (!rows.length) throw new Error("No applicants selected for PDF export.");
  openPdfWindow({ state, title: "Offline Form PDF", pagesHtml: rows.map(offlinePage).join(""), mode: "offline" });
}
